use std::thread;

pub fn invoke() {
    let vec1 = vec![1, 2, 3];
    // 2.强制闭包取得捕获变量的所有权 move
    // *************************************************************
    // 常用于闭包的生命周期大于捕获变量的生命周期时，例如将闭包返回或移入其他线程
    // *************************************************************
    let handle = thread::spawn(move || {
        println!("vec1 is :{:?}", vec1);
    });
    handle.join().unwrap();
}